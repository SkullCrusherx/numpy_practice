#arethmetic operation in numpy
import numpy as np

#addition only single array with int
var_1 =np.array([1,2,3,4])
print(var_1+3)

#array + array adding
var_2 =np.array([1,2,3,4])
var_3 = np.array([1,2,3,4])
print(var_2+var_3)

#subtract with int
var_1 =np.array([1,2,3,4])
print(var_1-3)

#subtract array from array
var_2 =np.array([1,2,3,4])
var_3 = np.array([1,2,3,4])
print(var_2-var_3)


#multiply with int
var_1 =np.array([1,2,3,4])
print(var_1*3)

var_2 =np.array([1,2,3,4])
var_3 = np.array([1,2,3,4])
print(var_2*var_3)


